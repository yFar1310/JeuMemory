    //méthode qui remplit la JList avec les familles de tous les personnages
    private void initListeFamilles(){
        
        ArrayList<String>lst=this.lp.getFamilles();
        //On initialise le Modèle par la JList
        DefaultListModel mod = new DefaultListModel();
        //On affecte le modèle à la JList
        ListeFamilles.setModel(mod);
        //remplisage de la JComboBox
        for(int i=0;i<lst.size();i++)
            mod.addElement(lst.get(i));
    }

