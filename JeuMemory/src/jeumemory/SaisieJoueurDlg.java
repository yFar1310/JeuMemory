    //gestionnaire du clic sur Parcourir
    private void ParcourirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ParcourirActionPerformed
        JFileChooser jf= new JFileChooser();
        if (jf.showOpenDialog(this)== JFileChooser.APPROVE_OPTION)
        {
        String path = jf.getSelectedFile().getPath();
        Image img=Toolkit.getDefaultToolkit().getImage(path);
        img=img.getScaledInstance(Photo.getWidth()-10, Photo.getHeight()-10, Image.SCALE_DEFAULT);
        this.img = new ImageIcon(img);
        Photo.setIcon(this.img);
 } 
    }//GEN-LAST:event_ParcourirActionPerformed
    //gestionnaire du clic sur Valider
    private void ValiderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ValiderActionPerformed
       if(!pseudo.getText().equals("") && !ListeFamilles.getSelectedValue().equals("")){
          this.j=new Joueur(pseudo.getText(),ListeFamilles.getSelectedValue());
             this.ok=true;
             if(j.getPhoto()!=null)
             {
              j.setPhoto((ImageIcon) Photo.getIcon());
             
             }
             
              setVisible(false);
              dispose();
              
                    
       }
        else
            pseudo.setText("Erreur pseudo nul");
             
    }//GEN-LAST:event_ValiderActionPerformed
